# 2. 데이터프레임 생성 및 가공
df = pd.DataFrame(data, columns=['Date', 'Amount', 'Liters', 'UnitPrice'])
df['Date'] = pd.to_datetime(df['Date'], format='%Y%m%d')
df['Year'] = df['Date'].dt.year

# 3. 연도별 요약 통계 계산
yearly_summary = df.groupby('Year').agg({
    'Amount': ['count', 'sum', 'mean'],
    'Liters': 'sum'
}).reset_index()

yearly_summary.columns = ['연도', '주유횟수', '총지출액', '평균주유비', '총주유량']

print("=== 연도별 주유 요약 보고서 ===")
print(yearly_summary.to_string(index=False))

# 4. 시각화 (최근 주유 트렌드)
